/** Apply template variant defaults to a slide. Slide-level fields win, so
 *  AI-generated kicker/title/etc remain. The template only fills in
 *  variant slots that the slide hasn't already set. */
export function applyTemplateToSlide(slide: Slide, t: TemplateVariantDefaults): Slide {
  return {
    ...slide,
    titleVariant:    slide.titleVariant    ?? t.titleVariant,
    bulletsVariant:  slide.bulletsVariant  ?? t.bulletsVariant,
    twoColumnVariant:slide.twoColumnVariant?? t.twoColumnVariant,
    tableVariant:    slide.tableVariant    ?? t.tableVariant,
    quoteVariant:    slide.quoteVariant    ?? t.quoteVariant,
    sectionVariant:  slide.sectionVariant  ?? t.sectionVariant,
    closingVariant:  slide.closingVariant  ?? t.closingVariant,
  };
}

export function getTemplate(id?: string): DeckTemplate | undefined {
  return DECK_TEMPLATES.find((t) => t.id === id);
}
