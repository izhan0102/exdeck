/**
 * Deck templates: curated combinations of theme + font + graphic + per-layout
 * variants + a sample prompt. Picking a template skips the theme/font/graphic
 * steps and jumps straight to "edit prompt and generate".
 *
 * Variant defaults are applied to every slide in the generated deck so the
 * whole deck inherits a cohesive visual language (e.g. "Editorial Sand" makes
 * every quote slide use the editorial pull-quote variant, every title-hero
 * use the underlined variant, etc.).
 */

import type { ContentDensity, Slide } from "./types";

export type TemplateVariantDefaults = {
  titleVariant?: Slide["titleVariant"];
  bulletsVariant?: Slide["bulletsVariant"];
  twoColumnVariant?: Slide["twoColumnVariant"];
  tableVariant?: Slide["tableVariant"];
  quoteVariant?: Slide["quoteVariant"];
  sectionVariant?: Slide["sectionVariant"];
  closingVariant?: Slide["closingVariant"];
};

export type DeckTemplate = {
  id: string;
  name: string;
  /** One-line description shown under the title. */
  tagline: string;
  /** Short label used as a chip (Pitch, Lecture, Report, etc.). */
  category: string;
  themeId: string;
  fontId: string;
  graphicId: string;
  /** Optional accent override for the graphic. */
  graphicAccent?: string;
  /** Generation defaults. */
  density?: ContentDensity;
  includeReferences?: boolean;
  /** Sample prompt seeded into the brief so the user can tweak it. */
  samplePrompt: string;
  /** Per-layout visual variants applied to every slide after generation. */
  variants: TemplateVariantDefaults;
  /** Show a "NEW" badge on the gallery card. */
  isNew?: boolean;
};

